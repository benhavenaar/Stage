            uicontrol(self.valleyPanel, 'Style', 'text',...
                'String', 'Prominence:', 'Units', 'normalized',...
                'Position', [.01, .78, .25, .1]);
            uicontrol(self.valleyPanel, 'Style', 'text',...
                'String', 'Duration:     ', 'Units', 'normalized',...
