                'fontSize', 12, 'Position', [.47, .55, .525, .4]);
            uicontrol(self.informationPanel, 'Style', 'text',...
                'fontSize', 10, 'String', 'Amplitude:',...
                'Units', 'normalized', 'Position', [.05, .8, .1, .1]);
