                'String', num2str(self.slopeDuration), 'Units', 'normalized',...
                'Position', [.3, .42, .2, .2]);
            
            self.channelNumber = uicontrol(self.channelPanel, 'Style', 'edit',...
                'String', num2str(self.channel), 'Units', 'normalized',...
                'Position', [.3, .72, .2, .2],...
